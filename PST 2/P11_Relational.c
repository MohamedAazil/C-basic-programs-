//Program for Relational Operators//
#include <stdio.h>
int main()
{
    int x = 10;
    int y = 5;
    
    printf("x>y is %d\n",x>y);
    printf("x>y is %d\n",x<y);
    printf("x=y is %d\n",x==y);
    printf("x!=y is %d\n",x!=y);
    printf("x>=y is %d\n",x>=y);
    printf("x<=y is %d\n",x<=y);
    return 0;
}